// Area and Perimeter of Rectangle

#include <cmath>
#include <iostream>

int main() {
    std::cout << "For a rectangle that has dimensions\n";
    std::cout << "That has a length of 8.9 cm and a width of 6.7 cm: \n";
    std::cout << "\n";
    std::cout << "Area - " << 8.9 * 6.7 << " cm^2\n";
    std::cout << "Perimeter - " << (2 * (8.9 + 6.7)) << "\n";
}
